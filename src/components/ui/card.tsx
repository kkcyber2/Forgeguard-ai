import * as React from "react";
import { cn } from "@/lib/utils";

/**
 * Card
 * ----
 * Flat surface. Hairline border at 0.5px, micro-radius, subtle inner
 * highlight on the top edge. No ambient drop-shadow — the highlight
 * alone gives the panel its lift.
 */
export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "relative bg-surface rounded-sm border-hairline border-white/[0.06]",
      "shadow-[inset_0_1px_0_rgba(255,255,255,0.04)]",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

